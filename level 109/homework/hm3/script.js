//  იმ შემთხვევაში თუ რიცხვი ლუწია მაშინ ორზე უნდა გაყოს. კენტის შემთხვევაში გაამრავლოს 3-ზე და 1 დაუმატოს.
//  თქვენი მიზანია დაითვალოთ რამდენი ნაბიჯი დასჭირდათ 1-მდე მისასვლელად,

let number = Number(prompt())
let steps = 0

do{
    if(number % 2 === 0){
        number = number / 2
    }
    else{
        number = number * 3 + 1
    }
    steps++
} while (number !== 1)

