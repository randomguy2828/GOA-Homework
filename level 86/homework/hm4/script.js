//შეიყვანე ასაკი და ენერგიის დონე (1–10).,
//თუ ასაკი 20-ზე ნაკლებია და ენერგია მეტია 7-ზე → "გირჩევ სირბილს!",
//თუ ენერგია ნაკლებია 4-ზე → "გირჩევ დასვენებას!",
//სხვა შემთხვევაში → "გააგრძელე ჩვეულებრივად!"

const age = Number(prompt("age"));
const energy = Number(prompt("(1-10)"));

if(age < 20 && energy > 7) {
    console.log("გირჩევ სირბილს!");
}
else if(energy < 4) {
    console.log("გირჩევ დასვენებას!");
}
else {
    console.log("გააგრძელე ჩვეულებრივად!")
}