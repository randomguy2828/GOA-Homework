//შეიყვანე რიცხვი 1–10.,
//თუ ტოლია 7-ის ან 3-ის → "გაიმარჯვე!",
//თუ მეტი 8-ზე → "ძალიან მაღალი!",
//თუ ნაკლებია 2-ზე → "ძალიან დაბალია!",
//სხვა შემთხვევაში → "ცადე კიდევ!".

const num = Number(prompt("1-10"));

if(num === 7 || 3 === num) {
    console.log("გაიმარჯვე!");
}
else if(num > 8) {
    console.log("ძალიან მაღალი!");
}
else if(num < 2) {
    console.log("ძალიან დაბალია!");
}
else {
    console.log("ცადე კიდევ!");
}