//while loop ის დახმარებით გამოიტანეთ რიცხვები 0 იდან 10 მდე კონსოლში.

let i = 0
while(i < 10){
    console.log(i);
    i++
}

//while loop ის დახმარებით გამოიტანეთ თქვენი სახელი 20 ჯერ კონსოლში

let a = 1
while(a < 20){
    console.log("Giorgi");
    a++
}

//დაატრიალეთ while loop 1 იდან 20 მდე და გამოიტანეთ კონსოლში ყველა ლუწი რიცხვი(გამოიყენეთ if else)

let b = 1
while(b < 20){
    if (b % 2 === 0){
        console.log(b + "ლუწი");
    }
    b++
}

