let num = prompt("შეიყვანე რიცხვი");
num = Number(num);

// if სტრუქტურა ამოწმებს პირობას
if (num > 0) {
  // თუ რიცხვი მეტია 0ზე
  console.log("დადებითია");
} else if (num < 0) {
  // თუ წინა პირობა არ შესრულდა და რიცხვი ნაკლებია 0-ზე
  console.log("უარყოფითია");
} else {
  // თუ არც ერთი პირობა არ შესრულდა
  console.log("ნულია");
}